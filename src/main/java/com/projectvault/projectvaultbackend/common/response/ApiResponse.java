package com.projectvault.projectvaultbackend.common.response;

import org.springframework.http.HttpHeaders;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseCookie;
import org.springframework.http.ResponseEntity;

import java.time.LocalDateTime;

public class ApiResponse {
    public static <T> ResponseEntity<ResponseStructureEntity<T>> success(T data, String message, HttpStatus statusCode){
        ResponseStructureEntity<T> responseBody = ResponseStructureEntity.<T>builder()
                .status("success")
                .message(message)
                .data(data)
                .timestamp(LocalDateTime.now())
                .build();
        return ResponseEntity.status(statusCode).body(responseBody);
    }

    public static ResponseEntity<ErrorStructureEntity> error(String details, String message, HttpStatus statusCode){
        ErrorStructureEntity errorBody = ErrorStructureEntity.builder()
                .status("error")
                .message(message)
                .details(details)
                .timestamp(LocalDateTime.now())
                .build();
        return ResponseEntity.status(statusCode).body(errorBody);
    }

    public static <T> ResponseEntity<ResponseStructureEntity<T>> successWithCookie(
            T data, String message, HttpStatus statusCode, ResponseCookie... cookies) {

        ResponseStructureEntity<T> responseBody = ResponseStructureEntity.<T>builder()
                .status("success")
                .message(message)
                .data(data)
                .timestamp(LocalDateTime.now())
                .build();

        HttpHeaders headers = new HttpHeaders();
        for (ResponseCookie cookie : cookies) {
            headers.add(HttpHeaders.SET_COOKIE, cookie.toString());
        }

        return ResponseEntity.status(statusCode)
                .headers(headers)
                .body(responseBody);
    }

}
